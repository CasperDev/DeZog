	; forward wo sub
	NOP
	JR Z,LBL_0100_A

		NOP

LBL_0100_A:
	NOP
	RET


	DEFS 0x0200-$
	; forward
	CALL SUB_0204
	RET

SUB_0204:
	NOP
	JR Z,.L1

		NOP

.L1:
	NOP
	RET


	DEFS 0x0300-$
	; backward
	CALL SUB_0304
	RET

SUB_0304:
	NOP

.LOOP:
		NOP
		DJNZ .LOOP

	RET


	DEFS 0x0400-$
	; backward, 2 blocks
	CALL SUB_0404
	RET

SUB_0404:
	NOP

.LOOP:
		NOP
		CALL SUB_0080

		DJNZ .LOOP

	RET


	DEFS 0x0500-$
	; forward, mixing branches
	CALL SUB_0504
	RET

